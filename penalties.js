function getPenalty(scoreDifference) {
    let penaltyLevel = scoreDifference <= 500 ? "light" : scoreDifference <= 1000 ? "medium" : "hard";
    const penaltyList = penalties[penaltyLevel];
    return penaltyList[Math.floor(Math.random() * penaltyList.length)];
}